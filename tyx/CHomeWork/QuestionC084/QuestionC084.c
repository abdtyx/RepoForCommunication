/// <summary>
/// 输入两个字符串，统计第2个字符串（至少含2字符）
/// 在第1个字符串中出现的次数。没有出现，次数为0.
/// </summary>
/// <input>两个字符串，以空格分隔</input>
/// <output>出现次数，整数</output>
/// <sample>
/// banana  an
/// 2
/// </sample>
/// <author>Cui Shuning (崔舒宁）</author>
/// <difficulty>03</difficulty>
#define _CRT_SECURE_NO_WARNINGS

#include<stdio.h>

int main()
{
	char in[500];
	char matchStr[20];

	(void)scanf("%s%s", in, matchStr);
	int sum = 0;
	int i = 0;
	int j = 0;
	while (in[i])
	{
		int flag = 0;
		while (matchStr[j])
		{
			if (in[i] != matchStr[j])
			{
				flag = 1;
				break;
			}
			i++;
			j++;
		}
		flag == 0 ? sum++ : i++;
		j = 0;
	}
	printf("%d", sum);
	return 0;
}
